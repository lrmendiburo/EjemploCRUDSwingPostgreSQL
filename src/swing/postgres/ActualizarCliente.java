    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = jTextFieldNombreActualizar.getText();
        String nit = jTextFieldNITAcualizar.getText();

        if (!nombre.isEmpty() && (!nit.isEmpty() && Tools.isNumeric(nit))) {
            int nitInt = Integer.parseInt(nit);
            Cliente cliente = new Cliente(nitInt, nombre);
            BaseDato.actualizar(cliente, nitOld);
            jTextFieldNombreActualizar.setText("");
            jTextFieldNITAcualizar.setText("");
            Notificaciones notificaciones= new Notificaciones();
            notificaciones.actualizacionExitosa();
        } else {
            Notificaciones alerta = new Notificaciones();
            alerta.vacioOInvalido();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


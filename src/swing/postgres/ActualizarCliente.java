    public void mostrarDatos(int Nit) {
        ArrayList<Cliente> listaC = BaseDato.leer();
        for (Cliente c : listaC) {
            if (c.getNit() == Nit) {
                jTextFieldNombreActualizar.setText(c.getNombre());
                jTextFieldNITAcualizar.setText(c.getNit()+"");
            }
        }
    }
    
    
